package Exercise.FirstExercise;

/**
 * Write an infinite loop using do while condition.
 */

public class Pgm06 {
    public static void infiniteLoop() {
        do {
            System.out.println("HI"); // we can keep the body empty
        } while (true);
    }

    public static void main(String[] args) {
        infiniteLoop();
    }
}
